void run() {
    rd(x, n);
    vector<pLI> vec[20];
    int k;
    ll q;
    for (int i = 1; i <= n; ++i) {
        rd(k, q);
        vec[k].push_back(pLI(q * ten[k], i));
    }
    priority_queue<pLI> pq;
    ll sum = 0;
    ll need = 0;
    ll base = 1;
    vector<int> res;
    for (int i = 0; i <= 18; ++i) {
        for (auto &it : vec[i]) {
            pq.push(it);
        }
        need += base * (x % 10);
        base *= 10;
        x /= 10;
        while (sum < need) {
            if (pq.empty())
                return pt(-1);
            sum += pq.top().fi;
            res.push_back(pq.top().se);
            pq.pop();
        }
    }
    sort(all(res));
    pt(SZ(res));
    pt(res);
}

